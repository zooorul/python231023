#주민등록번호체크.py

import re

# 정규 표현식 패턴
rrn_pattern = r'\d{6}-[12]\d{6}'
# \d{6}: 앞 6자리는 연도, 월, 일로 이루어져야 합니다.
# -: 하이픈으로 앞쪽과 뒷쪽을 구분합니다.
# [12]\d{6}: 뒷 7자리는 1 또는 2로 시작하고, 그 뒤에 6자리 숫자가 옵니다.

# 주민등록번호를 검사할 텍스트
text = """
주민등록번호 목록:
1. 000101-1234567
2. 990202-2234567
3. 051231-1234567
4. 080310-2234567
5. 200504-3234567
6. 199912-4234567
7. 950607-1234567
8. 761212-2234567
9. 751001-3234567
10. 210105-1234567
"""

# 정규 표현식을 사용하여 주민등록번호 검색
rrns = re.findall(rrn_pattern, text)

# 유효한 주민등록번호만 출력
for i, rrn in enumerate(rrns, start=1):
    print(f"{i}. {rrn}")